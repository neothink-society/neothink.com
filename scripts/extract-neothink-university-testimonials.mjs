#!/usr/bin/env node
/**
 * Parse `migration/source/neothink-university.html` and emit
 * `lib/neothink-university-testimonials.generated.ts`.
 *
 * Run from repo root:
 *   node scripts/extract-neothink-university-testimonials.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const htmlPath = path.join(root, "migration/source/neothink-university.html");
const outPath = path.join(root, "lib/neothink-university-testimonials.generated.ts");

function decodeEntities(s) {
  return s
    .replace(/&#0*38;/g, "&")
    .replace(/&#x26;/gi, "&")
    .replace(/&amp;/g, "&")
    .replace(/&#0*39;/g, "'")
    .replace(/&#8217;/g, "'")
    .replace(/&#8216;/g, "'")
    .replace(/&#8211;/g, "–")
    .replace(/&#8220;/g, "\u201c")
    .replace(/&#8221;/g, "\u201d")
    .replace(/&#8230;/g, "…")
    .replace(/&hellip;/g, "…")
    .replace(/&quot;/g, '"')
    .replace(/&#(\d+);/g, (_, n) => String.fromCodePoint(Number(n)));
}

function stripTags(s) {
  return decodeEntities(s.replace(/<[^>]+>/g, " ")).replace(/\s+/g, " ").trim();
}

function htmlBrToParagraphs(inner) {
  const parts = inner.split(/<br\s*\/?>/gi);
  return parts.map((p) => stripTags(p)).filter(Boolean);
}

function parseTestimonials(html) {
  const chunks = html.split("wpmtst-testimonial testimonial t-slide post-");
  const items = [];

  for (let i = 1; i < chunks.length; i++) {
    const chunk = chunks[i];
    const idMatch = chunk.match(/^(\d+)/);
    if (!idMatch) continue;
    const wpId = idMatch[1];

    let image = null;
    const imgTag = chunk.match(/<img[^>]+>/);
    if (imgTag) {
      const t = imgTag[0];
      const srcM = t.match(/src=["']([^"']+)["']/);
      const wM = t.match(/\bwidth=["']?(\d+)["']?/);
      const hM = t.match(/\bheight=["']?(\d+)["']?/);
      const altM = t.match(/alt=["']([^"']*)["']/);
      if (srcM) {
        const src = decodeEntities(srcM[1].replace(/&amp;/g, "&"));
        image = {
          src,
          width: wM ? Number(wM[1]) : 100,
          height: hM ? Number(hM[1]) : 100,
          alt: altM ? decodeEntities(altM[1]).trim() || `Photo — member ${wpId}` : `Photo — member ${wpId}`,
        };
      }
    }

    let excerpt = "";
    const exIdx = chunk.indexOf("readmore-excerpt");
    if (exIdx !== -1) {
      const after = chunk.slice(exIdx);
      const end = after.indexOf("readmore-content");
      const block = end === -1 ? after : after.slice(0, end);
      const pieces = [...block.matchAll(/<div style="display:inline;">([^<]*)<\/div>/g)]
        .map((m) => m[1])
        .filter((t) => !/^\s*…\s*$/.test(t) && !t.includes("ellipsis"));
      excerpt = decodeEntities(pieces.join(" ").replace(/\s+/g, " ").trim());
    }

    let fullParagraphs = [];
    const fullM = chunk.match(/readmore-content[^>]*>\s*([\s\S]*?)<\/div>\s*<p><a/);
    if (fullM) fullParagraphs = htmlBrToParagraphs(fullM[1]);

    if (fullParagraphs.length === 0) {
      const inContent = chunk.indexOf("testimonial-content");
      const beforeFields = chunk.indexOf('testimonial-field name">');
      const slice =
        inContent !== -1 && beforeFields !== -1 ? chunk.slice(inContent, beforeFields) : chunk;
      const firstP = slice.match(/<p>([\s\S]*?)<\/p>/);
      if (firstP) {
        const text = stripTags(firstP[1]);
        if (text) fullParagraphs = [text];
      }
    }

    if (!excerpt && fullParagraphs.length) {
      const one = fullParagraphs.join(" ");
      excerpt = one.length > 200 ? `${one.slice(0, 197)}…` : one;
    }

    const nameM = chunk.match(/testimonial-field name">([^<]*)</);
    const name = nameM ? stripTags(nameM[1]) : "Member";

    let subtitle;
    const subM = chunk.match(
      /testimonial-field name">[^<]*<\/div>\s*<div class="wpmtst-testimonial-field testimonial-field ">([^<]+)<\/div>/
    );
    if (subM) subtitle = stripTags(subM[1]);

    items.push({
      id: wpId,
      name,
      subtitle,
      excerpt,
      fullParagraphs,
      image,
    });
  }

  return items;
}

function tsEscape(s) {
  return s.replace(/\\/g, "\\\\").replace(/'/g, "\\'").replace(/\r/g, "").replace(/\n/g, "\\n");
}

const html = fs.readFileSync(htmlPath, "utf8");
const items = parseTestimonials(html);

const lines = [];
lines.push("/**");
lines.push(" * AUTO-GENERATED by `scripts/extract-neothink-university-testimonials.mjs`");
lines.push(" * from `migration/source/neothink-university.html` — do not edit by hand.");
lines.push(" */");
lines.push("");
lines.push("export type NuTestimonialImage = {");
lines.push("  readonly src: string;");
lines.push("  readonly width: number;");
lines.push("  readonly height: number;");
lines.push("  readonly alt: string;");
lines.push("};");
lines.push("");
lines.push("export type NuTestimonial = {");
lines.push("  readonly id: string;");
lines.push("  readonly name: string;");
lines.push("  readonly subtitle?: string;");
lines.push("  readonly excerpt: string;");
lines.push("  readonly fullParagraphs: readonly string[];");
lines.push("  readonly image: NuTestimonialImage | null;");
lines.push("};");
lines.push("");
lines.push(`export const NEOTHINK_UNIVERSITY_TESTIMONIALS: readonly NuTestimonial[] = [`);

for (const it of items) {
  const img =
    it.image == null
      ? "null"
      : `{ src: '${tsEscape(it.image.src)}', width: ${it.image.width}, height: ${it.image.height}, alt: '${tsEscape(it.image.alt)}' }`;
  const subs = it.subtitle ? `subtitle: '${tsEscape(it.subtitle)}', ` : "";
  const paras = it.fullParagraphs.map((p) => `'${tsEscape(p)}'`).join(", ");
  lines.push(
    `  { id: '${it.id}', name: '${tsEscape(it.name)}', ${subs}excerpt: '${tsEscape(it.excerpt)}', fullParagraphs: [${paras}], image: ${img} },`
  );
}

lines.push("] as const;");
lines.push("");

fs.writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(`Wrote ${items.length} testimonials to ${path.relative(root, outPath)}`);
